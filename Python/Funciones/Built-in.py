
# MAX: numero mayor de una lista
lista = [1, 8, 5, 4]
print(max(lista))

# MIN: numero menor de una lista
print(min(lista))

# ROUND: Redondea a 6 decimales

numero = round(1.23456789, 3)

print(numero)

## BOOL: devuelve false si como parametro se pasa cero, false, vacio, ninguno / true si un distinto de cero, datos no vacios

resultado_bool = bool(0)

print(resultado_bool)

# ALL: retorna true si todos los valores son verdaderos, cadenas, etc

resultado_bool = all([1, True, (1, 2, 3)])

print(resultado_bool)

# SUM: suma de todos los valores de un iterable

suma_total = sum(lista)

print(suma_total)